// Unary operator is for increment and decrement
//x++ is postfix, --x is prefix
void main(){
  var x = 10;
  print(x++);
  print(x++);
  print('\n');

  var y = 15;
  print(y--);
  print(y--);
  print('\n');

  var z = 20;
  print(++z);
  print(++z);
  print('\n');

  var a =25;
  print(--a);
  print(--a);
  print('\n');
}